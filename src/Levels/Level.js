class Level {
  constructer(map, canvas, ctx) {
    this.canvas = canvas;
    this.ctx = ctx;
    this.map = map;
     
    this.gridWidth = canvas.width / map[0].length;
    this.gridHeight = canvas.height / map.length;

    this.drawLevel();
  }


  drawLevel() {
    this.map.forEach((row, i) => {
      let y = this.gridHeight * i;
      
      row.forEach((grid, j) => {
        let x = this.gridWidth * j;
        if (grid === 1) {
          this.ctx.beginPath();
          this.ctx.rect(x, y, this.gridWidth, this.gridHeight);
          // this.ctx.translate(this.width / 2, this.height / 2);
          this.ctx.fillStyle = "green";
          this.ctx.fill();
          this.ctx.closePath();

        }
      });
    });
  }

}

export default Level;

